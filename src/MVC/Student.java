package MVC;

public class Student {
    private String name;
    private String rollno;

    public void setName(String name){
        this.name = name;
    }
    public String getName(){
        return this.name;
    }
    public void setRollno(String rollno){
        this.rollno = rollno;
    }
    public String getRollno(){
        return this.rollno;
    }
}
